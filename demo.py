a = int(input("Enter 1st num: "))
b = int(input("Enter 2nd num: "))

def add(x, y):
    return x + y
def sub(x, y):
    return x - y
def mul(x, y):
    return x * y
def div(x, y):
    return x / y

print("Sum =", add(a, b))
print("Sub =", sub(a, b))
print("Mul =", mul(a, b))
print("Div =", div(a, b))

